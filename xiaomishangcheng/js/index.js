var shop =$('.shop');
shop.eq(0).on('mouseover',function(){
	$('.hover').eq(0).css('display','block').slideUp(2000);
	$(this).css('box-shadow','0 2px 4px 4px #eee');
	$(this).css('top','-2px')
})
shop.eq(0).on('mouseout',function(){
	
	$(this).css('box-shadow','none');
	$(this).css('top','0')
})
